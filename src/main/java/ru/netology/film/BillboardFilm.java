package ru.netology.film;

public class BillboardFilm {
    private int id;
    private  String filmName;
    private String filmGenre;
    private int count;


    public BillboardFilm(int id, String filmName, String filmGenre, int count) {
        this.id = id;
        this.filmName = filmName;
        this.filmGenre = filmGenre;
        this.count = count;


    }
}
